public class AlphabetUtil {
    public static String getLetre(int position) {
        switch (position) {
            case 0:
                return "A";
            case 1:
                return "B";
            case 2:
                return "C";
            case 3:
                return "D";
            case 4:
                return "G";
            case 5:
                return "H";
            case 6:
                return "I";
            case 7:
                return "J";
            case 8:
                return "L";
            case 9:
                return "M";
            case 10:
                return "N";
            case 11:
                return "O";
            case 12:
                return "P";
            case 13:
                return "R";
            case 14:
                return "S";
            case 15:
                return "T";
            case 16:
                return "U";
            case 17:
                return "V";
            case 18:
                return "W";
            case 19:
                return "Y";
            case 20:
                return "Z";
        }
        return "";
    }

}
